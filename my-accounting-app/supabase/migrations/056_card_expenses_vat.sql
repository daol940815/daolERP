-- 056_card_expenses_vat.sql
-- 법인카드 사용내역 부가세 분리 토대.
--
-- 카드사 파일(하나·롯데 등)에 부가세 컬럼이 있어 매입세액을 분리 저장한다.
-- 분개: 세액이 있으면 (차) 비용(공급가) + 부가세대급금(1102) / (대) 미지급금(총액)
--       세액이 없으면(우리카드 등) 기존대로 전액 비용.
-- → 월별 손익의 비용 정확화 + 부가세대급금 원장 대사(-43% 격차)의 재료.
-- =====================================================

ALTER TABLE card_expenses
  ADD COLUMN IF NOT EXISTS tax_amount BIGINT NOT NULL DEFAULT 0;

COMMENT ON COLUMN card_expenses.tax_amount IS '매입 부가세(카드사 파일 제공값). 0이면 분리 정보 없음 → 전액 비용 처리';
